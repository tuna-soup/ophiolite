#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(import.meta.dirname, "../../..");
const appRoot = path.resolve(import.meta.dirname, "..");
const policyPath = path.join(appRoot, "desktop-command-boundary.json");
const outputPath = path.join(appRoot, "src/lib/generated/desktop-bridge-stubs.ts");

const policy = JSON.parse(fs.readFileSync(policyPath, "utf8"));
const backendPath = path.join(repoRoot, policy.backend_handler_file);
const backendSource = fs.readFileSync(backendPath, "utf8");

function parseBackendHandlerCommands(source) {
  const match = source.match(/generate_handler!\s*\[([\s\S]*?)\]\s*\)/m);
  if (!match) {
    throw new Error("could not locate tauri::generate_handler! command list");
  }
  return match[1]
    .split(",")
    .map((value) => value.trim())
    .filter(Boolean)
    .sort();
}

function snakeToCamel(command) {
  return command
    .replace(/_command$/, "")
    .split("_")
    .map((segment, index) =>
      index === 0 ? segment : segment.charAt(0).toUpperCase() + segment.slice(1)
    )
    .join("");
}

function buildOwnerMap(commands, groups) {
  const ownerByCommand = new Map();
  for (const command of commands) {
    const group = groups.find((candidate) => new RegExp(candidate.pattern).test(command));
    ownerByCommand.set(command, group?.owner ?? "desktop.unclassified");
  }
  return ownerByCommand;
}

const backendCommands = parseBackendHandlerCommands(backendSource);
const backendOnlyCommands = new Set(policy.backend_only_commands.map((entry) => entry.command));
const bridgeCommands = backendCommands.filter((command) => !backendOnlyCommands.has(command));
const ownerByCommand = buildOwnerMap(backendCommands, policy.command_groups);

function objectEntries(commands, ownerLookup = null) {
  return commands
    .map((command) => {
      const key = snakeToCamel(command);
      const suffix = ownerLookup
        ? `: { command: "${command}", owner: "${ownerLookup.get(command)}" }`
        : `: "${command}"`;
      return `  ${key}${suffix}`;
    })
    .join(",\n");
}

const file = `// Generated by apps/traceboost-demo/scripts/generate-desktop-bridge-stubs.mjs.
// Do not edit by hand.

export const desktopBridgeCommands = {
${objectEntries(bridgeCommands)}
} as const;

export const desktopBackendOnlyCommands = {
${objectEntries([...backendOnlyCommands].sort())}
} as const;

export const desktopCommandMetadata = {
${objectEntries(backendCommands, ownerByCommand)}
} as const;

export type DesktopBridgeCommandKey = keyof typeof desktopBridgeCommands;
export type DesktopBridgeCommandName =
  (typeof desktopBridgeCommands)[DesktopBridgeCommandKey];

export type DesktopBackendOnlyCommandKey = keyof typeof desktopBackendOnlyCommands;
export type DesktopBackendOnlyCommandName =
  (typeof desktopBackendOnlyCommands)[DesktopBackendOnlyCommandKey];

export type DesktopCommandKey = keyof typeof desktopCommandMetadata;
export type DesktopCommandName =
  (typeof desktopCommandMetadata)[DesktopCommandKey]["command"];

export const desktopBridgeCommandNames = Object.values(desktopBridgeCommands);
export const desktopBackendOnlyCommandNames = Object.values(desktopBackendOnlyCommands);
export const desktopCommandNames = Object.values(desktopCommandMetadata).map(
  (entry) => entry.command
);
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, file);
